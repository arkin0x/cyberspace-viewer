def binary_to_hex(binary_str):
    hex_str = ""
    for i in range(0, len(binary_str), 4):
        bits = binary_str[i:i+4]
        hex_digit = int(bits, 2)
        if hex_digit < 10:
            hex_char = str(hex_digit) 
        else:
            hex_char = chr(hex_digit + 55)
        hex_str += hex_char

    return hex_str

